package Controller.InOutOperations;

/**
 * Interfaz TotalCostInterface.
 * Define el comportamiento que deben implementar las clases que calculan el coste total de una operación.
 */
public interface TotalCostInterface {

    /**
     * Calcula el coste total asociado a una operación.
     * 
     * @return el coste total como un valor double
     */
    double CalculatedCost();
}
